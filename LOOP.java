import java.util.Scanner;

class LOOP {
    public static void main(String[] args) {
        int n;
        System.out.print("Enter the value for Cond..\n");
        Scanner ref = new Scanner(System.in);
        n = ref.nextInt();
        while (n >= 0) {
            System.out.println("SAKET KUMAR");
        }

        System.out.println("End the program");
    }
}